DATA SEGMENT
NUM DB 9
RES DD ?
DATA ENDS
STACK SEGMENT
DW 100 DUP(?)
TOS LABEL WORD
STACK ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA,SS:STACK
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AX,STACK
	MOV SS,AX
	LEA SP,TOS
	MOV DX,0H
	MOV AL,NUM
	MOV AH,0H
	CMP AL,9
	JE NINE
NINE2:	CALL FACT
	MOV CH,09H
	CMP CH,9
	JE NINE3
NINE4:	MOV WORD PTR RES[0],AX
	MOV WORD PTR RES[2],DX
	INT 03
	MOV AH,4CH
	INT 21H
	FACT PROC
		CMP AX,1
		JE EXIT
		PUSH AX
		DEC AX
		CALL FACT
		POP AX
		MUL WORD PTR RES[0]
		MOV WORD PTR RES[0],AX
		RET
EXIT:   MOV WORD PTR RES[0],1H
	RET
NINE:	DEC AL
	JMP NINE2
NINE3:	MOV BL,NUM
	MUL BX
	JMP NINE4
CODE ENDS
END START
	
